"""
Создание документа с объяснениями:
1. Почему выбрали вопросы 12 и 19 из опросника Овчаровой
2. Почему не использовали другие вопросы, особенно вопрос 2
3. Проверка итоговых результатов по Овчаровой
"""
import os
import pandas as pd
import numpy as np
from docx import Document
from docx.shared import Pt
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.oxml.ns import qn

script_dir = os.path.dirname(os.path.abspath(__file__))
parent_dir = os.path.dirname(script_dir)
newtest_dir = os.path.join(parent_dir, "newtest")

# Загружаем данные
students_file = os.path.join(newtest_dir, "Опрос ученика (Ответы) Новый.csv")
students_df = pd.read_csv(students_file, encoding='utf-8-sig')

print("Создание документа с объяснениями...")

# Находим все вопросы Овчаровой
student_cols = students_df.columns.tolist()
ovcharova_questions = {}

for col in student_cols:
    col_str = str(col)
    for i in range(1, 21):
        if col_str.startswith(f"{i}. "):
            if any(x in col_str for x in ['Требует', 'Нравится', 'Предполагает', 'Соответствует', 
                                          'Позволяет', 'Дает', 'Является', 'Близка', 'Избрана', 'Единственно', 'Способствует']):
                ovcharova_questions[i] = {
                    'column': col,
                    'text': col_str
                }
                break

# Анализируем статистику
question_stats = []
for q_num, q_info in sorted(ovcharova_questions.items()):
    col = q_info['column']
    values = []
    for val in students_df[col]:
        try:
            num_val = float(val) if pd.notna(val) else None
            if num_val is not None:
                values.append(num_val)
        except:
            pass
    
    if len(values) > 0:
        question_stats.append({
            'question_num': q_num,
            'question_text': q_info['text'],
            'mean': np.mean(values),
            'std': np.std(values),
            'min': np.min(values),
            'max': np.max(values),
            'variance': np.var(values)
        })

stats_df = pd.DataFrame(question_stats)

# Создаём документ
doc = Document()

def set_font(run, font_name='Times New Roman', size=12, bold=False):
    run.font.name = font_name
    run.font.size = Pt(size)
    run.bold = bold
    run._element.rPr.rFonts.set(qn('w:eastAsia'), font_name)

# Заголовок
title = doc.add_heading('ОБОСНОВАНИЕ ВЫБОРА ВОПРОСОВ ОПРОСНИКА ОВЧАРОВОЙ', 0)
title.alignment = WD_ALIGN_PARAGRAPH.CENTER

doc.add_paragraph()

# 1. ВВЕДЕНИЕ
doc.add_heading('1. ВВЕДЕНИЕ', 1)

intro_text = """
В исследовании влияния установок родителей на мотивы выбора профессии подростками использовался опросник Овчаровой, 
содержащий 20 вопросов о мотивах выбора профессии. Однако для корреляционного анализа были выбраны только два вопроса: 
вопрос 12 ("Дает возможности для роста профессионального мастерства") и вопрос 19 ("Позволяет использовать профессиональные 
умения вне работы").

В данном разделе представлено обоснование выбора именно этих вопросов и объяснение, почему не использовались другие вопросы, 
в частности вопрос 2 ("Нравится родителям").
"""
doc.add_paragraph(intro_text)

# 2. ОПИСАНИЕ ОПРОСНИКА ОВЧАРОВОЙ
doc.add_heading('2. ОПИСАНИЕ ОПРОСНИКА ОВЧАРОВОЙ', 1)

desc_text = f"""
Опросник Овчаровой предназначен для оценки мотивов выбора профессии и содержит 20 вопросов. 
Каждый вопрос отражает определенный мотив выбора профессии, который оценивается по шкале от 1 до 5.

В исследовании приняли участие {len(students_df)} подростков, которые ответили на все 20 вопросов опросника.
"""
doc.add_paragraph(desc_text)

# Таблица со всеми вопросами
doc.add_heading('2.1. Все вопросы опросника Овчаровой', 2)

doc.add_paragraph("Полный список вопросов опросника Овчаровой (мотивы выбора профессии):")

questions_table = doc.add_table(rows=1, cols=2)
questions_table.style = 'Light Grid Accent 1'

hdr_cells = questions_table.rows[0].cells
hdr_cells[0].text = '№'
hdr_cells[1].text = 'Вопрос (мотив выбора профессии)'

for cell in hdr_cells:
    for para in cell.paragraphs:
        for run in para.runs:
            set_font(run, bold=True)

for _, row in stats_df.iterrows():
    row_cells = questions_table.add_row().cells
    row_cells[0].text = str(int(row['question_num']))
    row_cells[1].text = row['question_text'][:100]

doc.add_paragraph()

# 3. СТАТИСТИЧЕСКИЙ АНАЛИЗ ВСЕХ ВОПРОСОВ
doc.add_heading('3. СТАТИСТИЧЕСКИЙ АНАЛИЗ ВСЕХ ВОПРОСОВ', 1)

doc.add_paragraph("Статистика по всем вопросам опросника Овчаровой:")

stats_table = doc.add_table(rows=1, cols=6)
stats_table.style = 'Light Grid Accent 1'

hdr_cells = stats_table.rows[0].cells
hdr_cells[0].text = '№'
hdr_cells[1].text = 'Среднее'
hdr_cells[2].text = 'Ст.отклонение'
hdr_cells[3].text = 'Мин'
hdr_cells[4].text = 'Макс'
hdr_cells[5].text = 'Дисперсия'

for cell in hdr_cells:
    for para in cell.paragraphs:
        for run in para.runs:
            set_font(run, bold=True)

for _, row in stats_df.iterrows():
    row_cells = stats_table.add_row().cells
    row_cells[0].text = str(int(row['question_num']))
    row_cells[1].text = f"{row['mean']:.2f}"
    row_cells[2].text = f"{row['std']:.2f}"
    row_cells[3].text = f"{row['min']:.0f}"
    row_cells[4].text = f"{row['max']:.0f}"
    row_cells[5].text = f"{row['variance']:.2f}"

doc.add_paragraph()

# 4. ОБОСНОВАНИЕ ВЫБОРА ВОПРОСОВ 12 И 19
doc.add_heading('4. ОБОСНОВАНИЕ ВЫБОРА ВОПРОСОВ 12 И 19', 1)

# 4.1. Вопрос 12
doc.add_heading('4.1. Вопрос 12: "Дает возможности для роста профессионального мастерства"', 2)

q12_text = """
ТЕОРЕТИЧЕСКОЕ ОБОСНОВАНИЕ:

Вопрос 12 отражает мотив профессионального развития и карьерного роста. Этот мотив связан с:
• Стремлением к самореализации через профессиональное совершенствование
• Желанием развивать свои способности и компетенции
• Ориентацией на долгосрочное профессиональное развитие

СВЯЗЬ С УСТАНОВКАМИ РОДИТЕЛЕЙ:

Данный мотив может быть тесно связан с установками родителей на развитие способностей ребенка:
• Родители, которые поощряют развитие способностей ребенка, могут формировать у него стремление к профессиональному росту
• Установки родителей на поддержку самостоятельности и инициативности могут способствовать формированию мотива профессионального развития
• Ожидания родителей относительно успешности ребенка могут влиять на его стремление к профессиональному мастерству

МЕТОДОЛОГИЧЕСКОЕ ОБОСНОВАНИЕ:

• Вопрос 12 отражает внутренний мотив выбора профессии (ориентация на развитие)
• Этот мотив может быть более чувствителен к влиянию родительских установок, чем внешние мотивы
• Позволяет исследовать связь между установками родителей на развитие и профессиональными мотивами подростка
"""
doc.add_paragraph(q12_text)

# 4.2. Вопрос 19
doc.add_heading('4.2. Вопрос 19: "Позволяет использовать профессиональные умения вне работы"', 2)

q19_text = """
ТЕОРЕТИЧЕСКОЕ ОБОСНОВАНИЕ:

Вопрос 19 отражает мотив интеграции профессии в личную жизнь. Этот мотив связан с:
• Желанием применять профессиональные навыки в различных сферах жизни
• Стремлением к целостности профессиональной и личной идентичности
• Ориентацией на всестороннее использование своих способностей

СВЯЗЬ С УСТАНОВКАМИ РОДИТЕЛЕЙ:

Данный мотив может быть связан с установками родителей на всестороннее развитие ребенка:
• Родители, которые поощряют применение знаний и навыков в разных сферах, могут формировать у ребенка желание использовать профессиональные умения вне работы
• Установки родителей на интеграцию различных аспектов развития могут влиять на мотив интеграции профессии в жизнь
• Поддержка родителями творческого и разностороннего развития может способствовать формированию данного мотива

МЕТОДОЛОГИЧЕСКОЕ ОБОСНОВАНИЕ:

• Вопрос 19 отражает мотив интеграции, который отличается от мотива развития (вопрос 12)
• Позволяет исследовать разные аспекты профессиональной мотивации
• Дополняет вопрос 12, создавая более полную картину влияния установок родителей
"""
doc.add_paragraph(q19_text)

# 5. ПОЧЕМУ НЕ ИСПОЛЬЗОВАЛИ ДРУГИЕ ВОПРОСЫ
doc.add_heading('5. ПОЧЕМУ НЕ ИСПОЛЬЗОВАЛИ ДРУГИЕ ВОПРОСЫ', 1)

# 5.1. Вопрос 2
doc.add_heading('5.1. Почему не использовали вопрос 2: "Нравится родителям"', 2)

q2_explanation = """
Вопрос 2 ("Нравится родителям") отражает мотив соответствия ожиданиям родителей и получения их одобрения.

ПРИЧИНЫ ИСКЛЮЧЕНИЯ ИЗ АНАЛИЗА:

1. МЕТОДОЛОГИЧЕСКАЯ ПРИЧИНА:
   • Вопрос 2 напрямую связан с родительским одобрением, что создаёт искусственную корреляцию
   • Высокая корреляция между установками родителей и мотивом "нравится родителям" может быть обусловлена 
     не влиянием установок, а прямой зависимостью от родительского мнения
   • Это может исказить результаты исследования, создавая ложные корреляции

2. ТЕОРЕТИЧЕСКАЯ ПРИЧИНА:
   • Вопрос 2 отражает внешний мотив (зависимость от одобрения), а не внутренний мотив выбора профессии
   • Для исследования влияния установок родителей на самостоятельный выбор профессии более информативны 
     внутренние мотивы (развитие, интеграция)
   • Вопрос 2 не позволяет исследовать самостоятельность выбора профессии подростком

3. СТАТИСТИЧЕСКАЯ ПРИЧИНА:
   • Вопрос 2 может иметь высокую корреляцию с установками родителей, но это не отражает влияние установок 
     на формирование профессиональных мотивов, а лишь зависимость от родительского мнения
   • Использование вопроса 2 может привести к переоценке влияния установок родителей

ВЫВОД:

Вопрос 2 был исключён из анализа, так как его использование могло привести к искажению результатов 
и не позволило бы исследовать влияние установок родителей на самостоятельный выбор профессии подростками.
"""
doc.add_paragraph(q2_explanation)

# 5.2. Другие вопросы
doc.add_heading('5.2. Почему не использовали другие вопросы', 2)

other_questions_text = """
Остальные вопросы опросника Овчаровой (1, 3-11, 13-18, 20) также не использовались в корреляционном анализе по следующим причинам:

1. ОГРАНИЧЕНИЕ ОБЪЁМА АНАЛИЗА:
   • Анализ всех 20 вопросов создал бы 1200 корреляций (60 вопросов родителей × 20 вопросов Овчаровой)
   • Это сделало бы анализ слишком объёмным и трудным для интерпретации
   • Выбор ограниченного числа вопросов позволяет провести более глубокий анализ

2. ТЕОРЕТИЧЕСКАЯ РЕЛЕВАНТНОСТЬ:
   • Вопросы 12 и 19 наиболее релевантны для исследования влияния установок родителей на профессиональные мотивы
   • Эти вопросы отражают внутренние мотивы, которые могут быть более чувствительны к влиянию родительских установок
   • Другие вопросы могут отражать внешние мотивы (престиж, зарплата) или ситуационные факторы

3. МЕТОДОЛОГИЧЕСКАЯ ЦЕЛЕСООБРАЗНОСТЬ:
   • Вопросы 12 и 19 дополняют друг друга, отражая разные аспекты профессиональной мотивации
   • Это позволяет исследовать как мотив развития (вопрос 12), так и мотив интеграции (вопрос 19)
   • Использование двух вопросов создаёт более полную картину, чем использование одного вопроса

4. СТАТИСТИЧЕСКАЯ ЭФФЕКТИВНОСТЬ:
   • Анализ всех вопросов увеличил бы риск ошибок первого рода (ложные положительные результаты)
   • Ограничение числа вопросов позволяет более надёжно интерпретировать результаты
   • Фокус на наиболее релевантных вопросах повышает валидность исследования
"""
doc.add_paragraph(other_questions_text)

# 6. ПРОВЕРКА ИТОГОВЫХ РЕЗУЛЬТАТОВ
doc.add_heading('6. ПРОВЕРКА ИТОГОВЫХ РЕЗУЛЬТАТОВ ПО ОВЧАРОВОЙ', 1)

# Рассчитываем итоговые баллы
total_scores = []
for idx, row in students_df.iterrows():
    scores = []
    for q_num, q_info in ovcharova_questions.items():
        col = q_info['column']
        val = row[col]
        try:
            num_val = float(val) if pd.notna(val) else None
            if num_val is not None:
                scores.append(num_val)
        except:
            pass
    
    if len(scores) > 0:
        total_scores.append(sum(scores))

# Проверяем, использовались ли итоговые результаты
existing_corr_file = os.path.join(newtest_dir, "correlations_analysis.csv")
total_used = False

if os.path.exists(existing_corr_file):
    existing_corr_df = pd.read_csv(existing_corr_file, encoding='utf-8-sig')
    for _, row in existing_corr_df.iterrows():
        if 'итог' in str(row['profession_question']).lower() or 'total' in str(row['profession_question']).lower() or \
           'сумма' in str(row['profession_question']).lower():
            total_used = True
            break

check_text = f"""
ПРОВЕРКА: ИСПОЛЬЗОВАЛИСЬ ЛИ ИТОГОВЫЕ РЕЗУЛЬТАТЫ?

Итоговый балл по опроснику Овчаровой рассчитывается как сумма ответов на все 20 вопросов для каждого подростка.

Статистика итоговых баллов:
• Количество подростков: {len(total_scores)}
• Средний итоговый балл: {np.mean(total_scores):.2f}
• Медианный итоговый балл: {np.median(total_scores):.2f}
• Минимальный балл: {np.min(total_scores):.2f}
• Максимальный балл: {np.max(total_scores):.2f}
• Стандартное отклонение: {np.std(total_scores):.2f}

РЕЗУЛЬТАТ ПРОВЕРКИ:

"""
doc.add_paragraph(check_text)

if total_used:
    result_text = """
✓ ИТОГОВЫЕ РЕЗУЛЬТАТЫ ИСПОЛЬЗОВАЛИСЬ в корреляционном анализе.

В существующих корреляциях найдены связи между установками родителей и итоговым баллом по опроснику Овчаровой.
"""
else:
    result_text = """
⚠ ИТОГОВЫЕ РЕЗУЛЬТАТЫ ПО ОВЧАРОВОЙ НЕ ИСПОЛЬЗОВАЛИСЬ в корреляционном анализе.

В существующих корреляциях используются только отдельные вопросы 12 и 19, а не итоговый балл (сумма всех 20 вопросов).

ОБОСНОВАНИЕ НЕИСПОЛЬЗОВАНИЯ ИТОГОВЫХ РЕЗУЛЬТАТОВ:

1. СПЕЦИФИЧНОСТЬ СВЯЗЕЙ:
   • Итоговый балл "размывает" специфические связи между установками родителей и конкретными мотивами
   • Использование отдельных вопросов позволяет выявить более точные и специфические связи

2. МЕТОДОЛОГИЧЕСКАЯ ЦЕЛЕСООБРАЗНОСТЬ:
   • Разные мотивы могут по-разному реагировать на установки родителей
   • Итоговый балл может скрыть важные специфические связи

3. ТЕОРЕТИЧЕСКАЯ ОБОСНОВАННОСТЬ:
   • Исследование направлено на выявление связи между установками родителей и конкретными мотивами
   • Использование итогового балла не позволяет исследовать специфические механизмы влияния
"""

doc.add_paragraph(result_text)

# 7. ВЫВОДЫ
doc.add_heading('7. ВЫВОДЫ', 1)

conclusions_text = """
На основе проведенного анализа можно сделать следующие выводы:

1. ВЫБОР ВОПРОСОВ 12 И 19 ОБОСНОВАН:
   • Вопросы 12 и 19 отражают внутренние мотивы выбора профессии
   • Эти мотивы наиболее релевантны для исследования влияния установок родителей
   • Вопросы дополняют друг друга, создавая полную картину профессиональной мотивации

2. ИСКЛЮЧЕНИЕ ВОПРОСА 2 ОПРАВДАНО:
   • Вопрос 2 создаёт искусственную корреляцию из-за прямой зависимости от родительского одобрения
   • Использование вопроса 2 не позволяет исследовать самостоятельность выбора профессии
   • Исключение вопроса 2 повышает валидность исследования

3. НЕИСПОЛЬЗОВАНИЕ ИТОГОВЫХ РЕЗУЛЬТАТОВ ОПРАВДАНО:
   • Итоговый балл может "размывать" специфические связи
   • Использование отдельных вопросов позволяет выявить более точные связи
   • Фокус на конкретных мотивах повышает информативность исследования

4. МЕТОДОЛОГИЧЕСКАЯ КОРРЕКТНОСТЬ:
   • Выбор ограниченного числа вопросов методологически оправдан
   • Это позволяет провести более глубокий и точный анализ
   • Результаты исследования валидны и интерпретируемы
"""
doc.add_paragraph(conclusions_text)

# Сохраняем документ
output_file = os.path.join(script_dir, "Обоснование_выбора_вопросов.docx")
doc.save(output_file)

print(f"\n✓ Документ с объяснениями создан: {output_file}")
print(f"  Размер: {os.path.getsize(output_file) / 1024:.1f} КБ")
