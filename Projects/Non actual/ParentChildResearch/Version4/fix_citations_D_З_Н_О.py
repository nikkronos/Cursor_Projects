def format_citation_with_authors(ref_numbers):
    """Форматирует цитату с указанием авторов и номеров"""
    authors_list = []
    for num in ref_numbers:
        if num in AUTHORS_MAP:
            authors_list.append(f"{AUTHORS_MAP[num]}, {num}")
        else:
            authors_list.append(str(num))
    return "[" + "; ".join(authors_list) + "]"

def fix_citations_in_document():
    """Исправляет цитирование в документе (только задачи Д, З, Н, О)"""
    v4 = Path(__file__).parent
    v7_file = v4 / "ВКР_ПОЛНЫЙ_ДОКУМЕНТ_v7.docx"
    
    if not v7_file.exists():
        print(f"ERROR: {v7_file} not found")
        return False
    
    print("=" * 70)
    print("ЗАДАЧИ Д, З, Н, О: Замена перечислений на цитирование")
    print("(Задачи Ж и П отменены)")
    print("=" * 70)
    
    doc = Document(str(v7_file))
    changes = []
    
    # Список мест, где нужно заменить перечисления на цитирование
    # ТОЛЬКО задачи Д, З, Н, О (Ж и П исключены)
    
    replacements = [
        # Д: Страница 7 - [3; 31; 22]
        {
            "search": "[3; 31; 22]",
            "replace": format_citation_with_authors([3, 31, 22]),
            "desc": "Д: Страница 7 - добавлено цитирование [3; 31; 22]"
        },
        # З: Страница 13 - [3; 12; 31] (уже исправлено на [3; 12; 31], но проверим)
        {
            "search": "[3; 12; 31]",
            "replace": format_citation_with_authors([3, 12, 31]),
            "desc": "З: Страница 13 - добавлено цитирование [3; 12; 31]"
        },
        # Н: Страница 20 - [3; 4] и [38]
        {
            "search": "[3; 4]",
            "replace": format_citation_with_authors([3, 4]),
            "desc": "Н: Страница 20 - добавлено цитирование [3; 4]"
        },
        {
            "search": "[38]",
            "replace": format_citation_with_authors([38]),
            "desc": "Н: Страница 20 - добавлено цитирование [38]"
        },
        # О: Страница 21 - [20; 21; 26]
        {
            "search": "[20; 21; 26]",
            "replace": format_citation_with_authors([20, 21, 26]),
            "desc": "О: Страница 21 - добавлено цитирование [20; 21; 26]"
        },
    ]
    
    # Применяем замены
    for para in doc.paragraphs:
        for repl in replacements:
            if repl["search"] in para.text:
                # Проверяем, что замена ещё не выполнена
                if repl["replace"] not in para.text:
                    para.text = para.text.replace(repl["search"], repl["replace"])
                    changes.append(repl["desc"])
                    print(f"  ✓ {repl['desc']}")
                else:
                    print(f"  ⚠ Пропущено (уже исправлено): {repl['desc']}")
    
    print(f"\nВнесено изменений: {len(changes)}")
    
    if changes:
        # Сохраняем
        doc.save(str(v7_file))
        print(f"\n✓ Документ обновлён: {v7_file.name}")
        return True
    else:
        print(f"\n⚠ Изменений не внесено (возможно, уже исправлено)")
        return False

if __name__ == '__main__':
    fix_citations_in_document()





