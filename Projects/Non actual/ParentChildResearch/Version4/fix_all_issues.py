# -*- coding: utf-8 -*-
"""
Скрипт для исправления всех замечаний А-П в документе v6 и создания v7
"""
from pathlib import Path
from docx import Document
import re

def fix_all_issues():
    """Исправляет все замечания А-П в документе"""
    v4 = Path(__file__).parent
    v6_file = v4 / "ВКР_ПОЛНЫЙ_ДОКУМЕНТ_v6.docx"
    
    if not v6_file.exists():
        print(f"ERROR: {v6_file} not found")
        return False
    
    print("Читаю документ v6...")
    doc = Document(str(v6_file))
    
    # Собираем все параграфы для анализа
    paragraphs = list(doc.paragraphs)
    
    print(f"Найдено {len(paragraphs)} параграфов")
    
    # Создаём список изменений
    changes = []
    
    # Анализируем каждый параграф
    for i, para in enumerate(paragraphs):
        text = para.text
        
        # А. Ведение - последнее предложение первого абзаца
        # Нужно найти первый абзац введения и переписать последнее предложение
        if "В условиях динамично меняющегося мира" in text:
            # Это первый абзац введения
            # Последнее предложение: "в противном случае могут быть потрачены впустую значительные временные и ресурсные затраты."
            # Нужно переписать логично
            new_text = text.replace(
                "в противном случае могут быть потрачены впустую значительные временные и ресурсные затраты.",
                "в противном случае возможны значительные временные и ресурсные потери."
            )
            if new_text != text:
                changes.append((i, "А", text[:50] + "...", new_text[:50] + "..."))
                para.text = new_text
        
        # Б. Страница 3 - добавить источник на предложение про самоопределение
        if "с одной стороны, признаётся ключевая роль семьи" in text and "[14]" not in text:
            # Нужно добавить источник после "самоопределении подростка"
            new_text = text.replace(
                "в профессиональном самоопределении подростка, а с другой",
                "в профессиональном самоопределении подростка [18], а с другой"
            )
            if new_text != text:
                changes.append((i, "Б", text[:50] + "...", new_text[:50] + "..."))
                para.text = new_text
        
        # Г. Страница 6 (в тексте это около строки 57) - указать источники
        if "Опросник взаимодействия родитель-ребенок (ВРР) И.М. Марковской" in text and "[9]" not in text:
            # Добавляем источник к Марковой
            new_text = text.replace(
                "Опросник взаимодействия родитель-ребенок (ВРР) И.М. Марковской",
                "Опросник взаимодействия родитель-ребенок (ВРР) И.М. Марковской [9]"
            )
            if new_text != text:
                changes.append((i, "Г-Маркова", text[:50] + "...", new_text[:50] + "..."))
                para.text = new_text
        
        if "опросника профессиональных предпочтений Дж. Голланда" in text and "[10]" not in text:
            # Добавляем источник к Голланду
            new_text = text.replace(
                "опросника профессиональных предпочтений Дж. Голланда",
                "опросника профессиональных предпочтений Дж. Голланда [10]"
            )
            if new_text != text:
                changes.append((i, "Г-Голланд", text[:50] + "...", new_text[:50] + "..."))
                para.text = new_text
        
        if "методика «Мотивы выбора профессии»" in text and "[11]" not in text:
            # Добавляем источник к методике
            new_text = text.replace(
                "методика «Мотивы выбора профессии»",
                "методика «Мотивы выбора профессии» [11]"
            )
            if new_text != text:
                changes.append((i, "Г-Методика", text[:50] + "...", new_text[:50] + "..."))
                para.text = new_text
        
        # К. Удалить графу "пол" на странице 16
        if "Пол: юноши — 25 человек" in text:
            # Удаляем строку про пол
            para.text = ""  # Удаляем весь параграф
            changes.append((i, "К", "Удалена графа 'пол'", ""))
        
        # М. "количество наблюдений" - заменить на "размер выборки" или "количество испытуемых"
        if "n = количество наблюдений" in text:
            new_text = text.replace(
                "n = количество наблюдений",
                "n = размер выборки (количество испытуемых)"
            )
            if new_text != text:
                changes.append((i, "М", text, new_text))
                para.text = new_text
    
    print(f"\nВнесено {len(changes)} изменений:")
    for idx, code, old, new in changes:
        print(f"  {code}: параграф {idx}")
    
    # Сохраняем как v7
    v7_file = v4 / "ВКР_ПОЛНЫЙ_ДОКУМЕНТ_v7.docx"
    doc.save(str(v7_file))
    print(f"\n✓ Сохранён документ: {v7_file.name}")
    
    return True

if __name__ == '__main__':
    fix_all_issues()





